"""Конвертеры путей для маршрутов каталога.

Встроенный django-конвертер ``slug`` принимает только ASCII-символы
(``[-a-zA-Z0-9_]+``), поэтому slug-и, сгенерированные с ``allow_unicode=True``
(например, кириллические), не попадают в маршрут. Здесь объявлен
пользовательский конвертер для unicode-слагов.
"""


class UnicodeSlugConverter:
    """Конвертер unicode-слагов для URL-маршрутов.

    Разрешает любые «словесные» символы (включая кириллицу), дефисы
    и подчёркивания — то же множество, что и ``slugify(allow_unicode=True)``.
    """

    regex = r'[-\w]+'

    def to_python(self, value):
        """Передаёт значение в представление без изменений."""
        return value

    def to_url(self, value):
        """Возвращает значение для подстановки в URL."""
        return value
